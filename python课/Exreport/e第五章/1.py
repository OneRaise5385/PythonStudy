from turtle import *
setup(800,600,600,100)
speed(10)
for i in range(3):
    color('black','black')
    pensize(2)
    begin_fill()
    forward(80)
    left(120)
    forward(80)
    left(120)
    forward(80)
    left(120)
    end_fill()
    for j in range(3):
        color('grey','grey')
        begin_fill()
        penup()
        forward(20)
        pendown()
        left(60)
        forward(20)
        left(120)
        forward(20)
        left(120)
        forward(20)
        end_fill()
        penup()
        right(120)
        forward(20)
        right(180)
        forward(80)
        left(120)
        pendown()
    penup()
    forward(40)
    left(60)
    pendown()
    color('grey','grey')
    begin_fill()
    forward(40)
    left(120)
    forward(40)
    left(120)
    forward(40)
    end_fill()
    penup()
    right(120)
    forward(40)
    left(120)
    forward(80)
    right(180)
    pendown()
mainloop()